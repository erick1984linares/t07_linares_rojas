# Decodificar mensaje encriptado
# M = python
# N = java
# P = java script
import os
#input
msg=os.sys.argv[1].upper()
#bucle
for letra in msg:
    if letra == "M":
        print("python")
    if letra == "N":
        print("java")
    if letra == "P":
        print("java script")

#fin_iterador

print("Fin del bucle")
